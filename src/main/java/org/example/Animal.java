package org.example;


abstract class Animal {
    private int timestamp;
    private String name;

    public Animal(String name) {
        this.name = name;
    }

    public int getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(int timestamp) {
        this.timestamp = timestamp;
    }

    public String getName() {
        return name;
    }
}



